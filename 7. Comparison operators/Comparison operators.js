//Comparison operators
// > >= < <= == !=

let aliAge = 20 , gediAge= 45;
console.log(aliAge > gediAge);  //false
console.log(aliAge >= gediAge);  //false

console.log(aliAge < gediAge);  //true
console.log(aliAge <= gediAge); // true

console.log(aliAge == gediAge); //false
console.log(aliAge != gediAge);  //true
console.log(aliAge === gediAge);  //false  //Strict equality operator


console.log('23' === 23);  //false
console.log('23' == 23);  //true

