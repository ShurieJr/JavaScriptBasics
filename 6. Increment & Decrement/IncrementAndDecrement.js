//Increment and decrement operators

let salary = 1000;

console.log(salary++);  //1000             //post Increment
console.log(salary);  // 1001

let age = 40;
console.log(++age); // 41    // pre Increment



let grade = 30;
console.log(--grade); //  pre decrement  // 29

let test = 90;
console.log(test--); // post decrement  // 90
console.log(test); // 89
