// if else if statement
let grade = 90;

if(grade >=1 && grade <= 100){
    if(grade >= 90)
        console.log("A");
    else if(grade >= 80)
        console.log("B");
    else if(grade >= 70)
        console.log("C");
    else
        console.log("F");
}
else{
    console.log("Invalid grade number");
}